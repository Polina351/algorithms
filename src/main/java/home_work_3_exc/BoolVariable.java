package home_work_3_exc;

public class BoolVariable {
    private boolean bool;
    private String s;

    BoolVariable() {
    }

    public boolean getBool() {
        return bool;
    }
    public String getS() {
        return s;
    }
    public void setBool(boolean bool) {
        this.bool = bool;
    }
    public void setS(String s) {
        this.s = s;
    }
}
